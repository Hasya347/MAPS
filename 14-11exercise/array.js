const nums = [1, 2, 3, 4, 5]

// Add new number at the end
nums.push(6)
console.log('push', nums)
// Add new number at the begining
nums.unshift(0)
console.log('unshift', nums)
// remove number at the end
nums.pop()
console.log('pop', nums)
// remove number at the begining
nums.shift()
console.log('shift', nums)